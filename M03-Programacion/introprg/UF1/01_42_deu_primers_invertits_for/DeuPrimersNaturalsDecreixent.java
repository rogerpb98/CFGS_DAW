/*
 * Programa que mostra els nombres de l'1 al 10
 * en sentit decreixent utilitzant el for
 */
public class DeuPrimersNaturalsDecreixent {
    public static void main(String[] args) {
        for (int numero = 10; numero >= 1; numero = numero - 1) {
            System.out.println(numero);
        }
    }
}
